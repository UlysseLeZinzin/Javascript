function mois(i) {
    //retourner la chaine de caractères correspondant au mois nupérique demandé
    //en utilisant un tableau

    //Tableau des mois
    let mois = ["Janvier", "Février", "Mars", "Avril", "Mai", "Juin", "Juillet",
        "Août", "Septembre", "Octobre", "Novembre", "Décembre"];
    //Retourner le mois demandé
    return mois[i - 1];
}
